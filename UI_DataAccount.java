    public void tabel(){
        DefaultTableModel tbl=new DefaultTableModel();
        tbl.addColumn("kode akun");
        tbl.addColumn("tipe akun");
        tbl.addColumn("kelompok akun");
        tbl.addColumn("nama akun");
        try{
            Statement st=(Statement)Koneksi.getConnection().createStatement();
            ResultSet rs=st.executeQuery("SELECT * FROM tbl_account");
            
            while(rs.next()){
                tbl.addRow(new Object[]{
                rs.getString("kodeakun"),
                rs.getString("tipe_akun"),
                rs.getString("kelompok_akun"),
                rs.getString("nama_akun"),
                });
                TblResult.setModel(tbl);
            }
//            JOptionPane.showMessageDialog(null,"Koneksi Berhasil");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Koneksi Gagal"+e.getMessage());
            
        }
        
    }


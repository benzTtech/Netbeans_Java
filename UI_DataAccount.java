    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        UI_Account ak=new UI_Account();
        int baris=TblResult.getSelectedRow();
        TableModel tm=TblResult.getModel();
        String kodeakun = TblResult.getValueAt(baris,0).toString();
        ak.textKodeAkun.setText(kodeakun);
        String kodeakun1 = TblResult.getValueAt(baris,0).toString();
        ak.textKodeAkun1.setText(kodeakun1);
        String tipeAkun = TblResult.getValueAt(baris,1).toString();
        if(tipeAkun=="Detail"){
            ak.radioDetail.isSelected();
        }else if(tipeAkun=="Header"){
            ak.radioHeader.isSelected();
        }
        String nama = TblResult.getValueAt(baris,3).toString();
        ak.textNamaAkun.setText(nama);
        ak.setVisible(true);
        ak.pack();
        ak.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
    }//GEN-LAST:event_btnUbahActionPerformed

